package ll.entidades.contabilidad;

public class RelaDoc {

    private String idOficinaR;
    private String idDocR;
    private String tipMonedaR;
    private String nroDocR;

    public String getIdOficinaR() {
        return idOficinaR;
    }

    public void setIdOficinaR(String idOficinaR) {
        this.idOficinaR = idOficinaR;
    }

    public String getIdDocR() {
        return idDocR;
    }

    public void setIdDocR(String idDocR) {
        this.idDocR = idDocR;
    }

    public String getTipMonedaR() {
        return tipMonedaR;
    }

    public void setTipMonedaR(String tipMonedaR) {
        this.tipMonedaR = tipMonedaR;
    }

    public String getNroDocR() {
        return nroDocR;
    }

    public void setNroDocR(String nroDocR) {
        this.nroDocR = nroDocR;
    }

}
