package cac.sanlorenzo.entidades.rrhh;

/**
 *
 * @author RenRio
 */
public class TipoPapeleta {

    private String idTipoPapeleta;
    private String descripcion;
    private String estado;

    public String getIdTipoPapeleta() {
        return idTipoPapeleta;
    }

    public void setIdTipoPapeleta(String idTipoPapeleta) {
        this.idTipoPapeleta = idTipoPapeleta;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

}
